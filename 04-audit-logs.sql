create or replace function public.undo_audit_log(p_log_id bigint)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_log public.audit_logs%rowtype;
  v_is_super_admin boolean;
  v_columns text;
begin
  select exists (
    select 1
    from public.profiles
    where profiles.id = auth.uid()
      and profiles.rol = 'super_admin'
  )
    into v_is_super_admin;

  if not v_is_super_admin then
    raise exception 'Solo un super_admin puede deshacer cambios.';
  end if;

  select *
    into v_log
  from public.audit_logs
  where id = p_log_id
  for update;

  if not found then
    raise exception 'No existe el registro de auditoria.';
  end if;

  if v_log.undone_at is not null then
    raise exception 'Este cambio ya fue deshecho.';
  end if;

  if v_log.table_name not in (
    'productos',
    'producto_variantes',
    'categorias',
    'imagenes_producto',
    'ordenes',
    'orden_items'
  ) then
    raise exception 'La tabla % no permite deshacer cambios.', v_log.table_name;
  end if;

  perform set_config('app.audit_skip', 'true', true);

  if v_log.action = 'INSERT' then
    execute format(
      'delete from public.%I where id = $1::bigint',
      v_log.table_name
    )
    using v_log.record_id;
  elsif v_log.action = 'DELETE' then
    execute format(
      'insert into public.%I select * from jsonb_populate_record(null::public.%I, $1)',
      v_log.table_name,
      v_log.table_name
    )
    using v_log.before_data;
  elsif v_log.action = 'UPDATE' then
    select string_agg(quote_ident(attname), ', ' order by attnum)
      into v_columns
    from pg_attribute
    where attrelid = format('public.%I', v_log.table_name)::regclass
      and attnum > 0
      and not attisdropped;

    execute format(
      'update public.%I set (%s) = (select %s from jsonb_populate_record(null::public.%I, $1)) where id = $2::bigint',
      v_log.table_name,
      v_columns,
      v_columns,
      v_log.table_name
    )
    using v_log.before_data, v_log.record_id;
  else
    raise exception 'Accion no soportada: %', v_log.action;
  end if;

  update public.audit_logs
    set undone_at = now(),
        undone_by = auth.uid()
  where id = p_log_id;

  return true;
end;
$$;

grant execute on function public.undo_audit_log(bigint) to authenticated;

notify pgrst, 'reload schema';
