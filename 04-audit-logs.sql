    create trigger categorias_audit_log_trigger
      after insert or update or delete on public.categorias
      for each row execute function public.audit_log_change();
  end if;

  if to_regclass('public.imagenes_producto') is not null then
    drop trigger if exists imagenes_producto_audit_log_trigger on public.imagenes_producto;
    create trigger imagenes_producto_audit_log_trigger
      after insert or update or delete on public.imagenes_producto
      for each row execute function public.audit_log_change();
  end if;

  if to_regclass('public.ordenes') is not null then
    drop trigger if exists ordenes_audit_log_trigger on public.ordenes;
    create trigger ordenes_audit_log_trigger
