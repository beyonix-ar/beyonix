create table if not exists public.audit_logs (
  id bigint generated by default as identity primary key,
  table_name text not null,
  action text not null check (action in ('INSERT', 'UPDATE', 'DELETE')),
  record_id text,
  actor_user_id uuid,
  actor_email text,
  before_data jsonb,
  after_data jsonb,
  created_at timestamptz not null default now(),
  undone_at timestamptz,
  undone_by uuid
);

create index if not exists audit_logs_created_at_idx
  on public.audit_logs(created_at desc);

create index if not exists audit_logs_table_name_idx
  on public.audit_logs(table_name);

alter table public.audit_logs enable row level security;

drop policy if exists "audit_logs_super_admin_select"
  on public.audit_logs;

create policy "audit_logs_super_admin_select"
  on public.audit_logs
  for select
  to authenticated
  using (
    exists (
      select 1
      from public.profiles
      where profiles.id = auth.uid()
        and profiles.rol = 'super_admin'
    )
  );

grant select on public.audit_logs to authenticated;

create or replace function public.audit_log_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor_user_id uuid;
  v_actor_email text;
  v_record_id text;
begin
  if current_setting('app.audit_skip', true) = 'true' then
    if tg_op = 'DELETE' then
      return old;
    end if;

    return new;
  end if;

  v_actor_user_id := auth.uid();

  select profiles.email
    into v_actor_email
  from public.profiles
  where profiles.id = v_actor_user_id;

  v_actor_email := coalesce(v_actor_email, auth.jwt() ->> 'email');

  if tg_op = 'DELETE' then
    v_record_id := old.id::text;
  else
    v_record_id := new.id::text;
  end if;

  insert into public.audit_logs (
    table_name,
    action,
    record_id,
    actor_user_id,
    actor_email,
    before_data,
    after_data
  )
  values (
    tg_table_name,
    tg_op,
    v_record_id,
    v_actor_user_id,
    v_actor_email,
    case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) else null end,
    case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) else null end
  );

  if tg_op = 'DELETE' then
    return old;
  end if;

  return new;
end;
$$;

do $$
begin
  if to_regclass('public.productos') is not null then
    drop trigger if exists productos_audit_log_trigger on public.productos;
    create trigger productos_audit_log_trigger
      after insert or update or delete on public.productos
      for each row execute function public.audit_log_change();
  end if;

  if to_regclass('public.producto_variantes') is not null then
    drop trigger if exists producto_variantes_audit_log_trigger on public.producto_variantes;
    create trigger producto_variantes_audit_log_trigger
